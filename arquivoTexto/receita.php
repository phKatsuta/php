<p>
    <b>Ingredientes para a massa</b>
</p>
<ul>
    <li>
        400g de macarrão espaguete
    </li>
    <li>
        Azeite a gosto
    </li>
    <li>
        Queijo ralado a gosto
    </li>
</ul>
<p>
    <b>Ingredientes para as almôndegas</b>
</p>
<ul>
    <li>
        1 kg de carne moída
    </li>
    <li>
        5 dentes de alho
    </li>
    <li>
        1/2 xícara de salsa picada
    </li>
    <li>
        1 xícara de queijo parmesão
    </li>
    <li>
        2 ovos
    </li>
    <li>
        2 pães amanhecidos
    </li>
    <li>
        Leite
    </li>
    <li>
        Sal e pimenta a gosto
    </li>
</ul>
<p>
    <b>Ingredientes para o molho</b>
</p>
<ul>
    <li>
        2kg de tomates maduros
    </li>
    <li>
        6 c. sopa de azeite
    </li>
    <li>
        1 cebola
    </li>
    <li>
        2 dentes de alho
    </li>
    <li>
        3 c. sopa de manjericão picado
    </li>
    <li>
        1 pitada de cominho
    </li>
    <li>
        Sal e pimenta a gosto
    </li>
</ul>

<p>
    <b>Modo de preparação</b>
</p>
<ol>
    <li>
        Para preparar as almôndegas, comece ralando os pães amanhecidos até formar uma farinha.
    </li>
    <li>
        Depois, misture num recipiente grande os dentes de alho picados, a salsa, o queijo, os 
        ovos, a farinha de pão e um pouco de sal e pimenta.
    </li>
    <li>
        Adicione a carne moída e envolva muito bem com as mãos, até que todos os ingredientes 
        estejam bem incorporados.
    </li>
    <li>
        Prepare as bolas de carne com as mãos untadas em azeite.
    </li>
    <li>
        Sele as almôndegas numa frigideira com azeite, virando-as até estarem com uma cor 
        uniforme. Reserve.
    </li>
    <li>
        Depois, prepare o molho de tomate, fazendo um refogado com o azeite, a cebola e os alhos 
        picados. Quando dourar junte os tomates aos pedaços e sem sementes.
    </li>
    <li>
        Deixe cozinhar, temperando com sal, pimenta, cominhos e manjericão, durante cerca de 20 
        minutos. No final, triture tudo com um mixer ou no liquidificador.
    </li>
    <li>
        Leve novamente ao fogo e junte-lhes as almôndegas e deixe cozinhar mais 10 minutos em 
        fogo médio.
    </li>
    <li>
        Cozinhe o espaguete em água com uma pitada de sal. Escorra.
    </li>
    <li>
        Sirva o espaguete com as almôndegas e muito molho.
    </li>
    <li>
        Polvilhe com queijo ralado e decore com uma folha de manjericão.
    </li>
</ol>

